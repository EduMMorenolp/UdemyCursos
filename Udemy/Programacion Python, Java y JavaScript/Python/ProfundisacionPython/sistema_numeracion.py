

#BASES DE NUMERACION
#Decimal - 10
a =10
print(f"a Decimal = ",{a})
#Binario 0b(numero) - 2
a = 0b1010
print(f"a Binario = ",{a})
#Octal 0o(numero) - 8
a = 0o12
print(f"a Octal = ",{a})
#Hexadecimal 0x(numero) - 16
a = 0xA
print(f"a hexadecimal = ",{a})

#Convertir tipo entero, incluyendo la base
# Decimal
b = int("23", 10)
print(f"b decimal = ",{b})
# Binario
b = int("10111", 2) # Numero 23
print(f"b binario = ",{b}) 
# Octal
b = int("27", 8) # Numero 23
print(f"b Octal = ",{b})
# Hexadecimal
b = int("17", 16) # Numero 23
print(f"b hexadecimal = ",{b})
# Base 5
x = int("344", 5) # Numero 99
print(f"x Base 5 = ",x)
